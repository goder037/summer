package com.rocket.summer.framework.expression;

/**
 * Supported operations that an {@link OperatorOverloader} can implement for any pair of
 * operands.
 *
 * @author Andy Clement
 * @since 3.0
 */
public enum Operation {

    ADD,

    SUBTRACT,

    DIVIDE,

    MULTIPLY,

    MODULUS,

    POWER

}

